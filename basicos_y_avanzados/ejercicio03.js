/*
Instrucciones: En este ejercicio podéis mostrar directamente los resultados por consola o almacenarlos en variables (esto lo podéis hacer en todos los demás, como os resulte más claro).

3.1 Multiplica 10 por 5 y muestra el resultado mediante console.
3.2 Divide 10 por 2 y muestra el resultado en un console.
3.3 Muestra mediante un console el resto de dividir 15 por 9.
3.4 Usa el correcto operador de asignación que resultará en o = 15,
teniendo dos variables p = 10 y j = 5.
3.5 Usa el correcto operador de asignación que resultará en i = 50,
teniendo dos variables c = 10 y m = 5.
*/

// 3.1 Multiplica 10 por 5 y muestra el resultado mediante console.
let a = 10;
let b = 5;
let multi = a * b;
console.log(`El resultado de multiplicar 10 por 5 es: ${multi}`);
// en consola: El resultado de multiplicar 10 por 5 es: 50

// 3.2 Divide 10 por 2 y muestra el resultado en un console.
let dividendo = 10;
let divisor = 2;
let division = dividendo / divisor;
console.log(`El resultado de dividir 10 por 2 es: ${division}`);
// en consola: El resultado de dividir 10 entre 2 es: 5

// 3.3 Muestra mediante un console el resto de dividir 15 por 9.
let num1 = 15;
let num2 = 9;
let resto = num1 % num2;
console.log(`El resto de dividir 15 por 9 es: ${resto}`);
// en consola: El resto de dividir 15 entre 9 es: 6

// 3.4 Usa el correcto operador de asignación que resultará en o = 15
// Teniendo dos variables p = 10 y j = 5.
let p = 10;
let j = 5;
let o = p + j; // Operador de asignación suma
console.log(`El valor de o es: ${o}`);
// resultado en pantalla: El valor de o es: 15

// 3.5 Usa el correcto operador de asignación que resultará en i = 50
// Teniendo dos variables c = 10 y m = 5.
let c = 10;
let m = 5;
let i = c * m; // Operador de asignación multiplicación
console.log(`El valor de i es: ${i}`);
// resultado en pantalla: El valor de i es: 50
