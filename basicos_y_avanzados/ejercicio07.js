// Completa esta función para que, al recibir dos números por argumento, te devuelva por consola el más alto de los dos.

function numMayor(numUno, numDos) {
  console.log(Math.max(numUno, numDos));
}

numMayor(5, 8); // Retorna 8
numMayor(2.56, 1.45); // Retorna 2.56
numMayor(2, 2); // Retorna 2
